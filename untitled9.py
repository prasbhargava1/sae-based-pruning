"""
Step 4 — Causal Feature Verification (All 32 Layers)
------------------------------------------------------
Tests which of the top-K candidate features from Step 3 actually
CAUSE sentiment predictions, not just correlate with them.

Method: ablation patching — zero out one feature at a time,
reconstruct the modified activation, run the linear probe,
measure how many predictions flip.

No Mistral needed — works entirely from saved activations on disk.

Efficiency trick:
  full_recon    = features @ W_dec + b_dec          (once per layer)
  ablated_recon = full_recon - feat[:,f] * W_dec[f] (per feature, cheap)

Inputs:
  {DRIVE_ROOT}/activations/layer_{N}_train.npy
  {DRIVE_ROOT}/sae_models/sae_layer_{N}.pt
  {DRIVE_ROOT}/feature_scores/layer_{N}_topk.npy

Output:
  {DRIVE_ROOT}/causal_features/layer_{N}_causal.npy        causal feature indices
  {DRIVE_ROOT}/causal_features/layer_{N}_causal_stats.npy  flip_rate + score_delta
"""

import os
import torch
import torch.nn as nn
import torch.nn.functional as F
import numpy as np
import matplotlib.pyplot as plt
from tqdm import tqdm

# ── Reproducibility ───────────────────────────────────────────────────────────

SEED = 42
torch.manual_seed(SEED)
np.random.seed(SEED)

# ── Config ────────────────────────────────────────────────────────────────────

TARGET_LAYERS  = list(range(32))
FLIP_THRESHOLD = 0.05    # feature must flip ≥5% of predictions to be causal
PROBE_STEPS    = 300
PROBE_LR       = 1e-3
PROBE_BATCH    = 256
N_TRAIN        = 2000

DEVICE = "cuda" if torch.cuda.is_available() else "cpu"

# ── Google Drive paths ────────────────────────────────────────────────────────

DRIVE_ROOT  = "/content/drive/MyDrive/sae_pruning"
