def get_thresholds(importance):
    return (
        float(np.percentile(importance, PRUNE_PERCENTILE)),
        float(np.percentile(importance, SCALE_PERCENTILE))
    )


def bucket_scale(score, prune_thresh, keep_thresh):
    if score >= keep_thresh:
        return 1.0
    elif score <= prune_thresh:
        return 0.0
    else:
        return (score - prune_thresh) / (keep_thresh - prune_thresh + 1e-8)


# \u2500\u2500 Pruning \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\n
def prune_mlp(layer, importance):
    prune_thresh, keep_thresh = get_thresholds(importance)
    n_pruned = n_scaled = 0

    with torch.no_grad():
        for i, score in enumerate(importance):
            scale = bucket_scale(float(score), prune_thresh, keep_thresh)
            if scale == 1.0:
                continue
            n_pruned += scale == 0.0
            n_scaled += 0.0 < scale < 1.0
            layer.mlp.gate_proj.weight.data[i, :].mul_(scale)
            layer.mlp.up_proj.weight.data[i, :].mul_(scale)
            layer.mlp.down_proj.weight.data[:, i].mul_(scale)

    return int(n_pruned), int(n_scaled)


def prune_attention(layer, importance):
    prune_thresh, keep_thresh = get_thresholds(importance)
    n_pruned = n_scaled = 0

    with torch.no_grad():
        for h in range(N_HEADS):
            scale = bucket_scale(float(importance[h]), prune_thresh, keep_thresh)
            if scale == 1.0:
                continue
            n_pruned += scale == 0.0
            n_scaled += 0.0 < scale < 1.0
            s, e = h * HEAD_DIM, (h + 1) * HEAD_DIM
            layer.self_attn.q_proj.weight.data[s:e, :].mul_(scale)
            layer.self_attn.o_proj.weight.data[:, s:e].mul_(scale)

        # Zero KV head only if ALL query heads in its GQA group are pruned
        for kv_h in range(N_KV_HEADS):
            g_start = kv_h * HEADS_PER_GROUP
            g_end   = g_start + HEADS_PER_GROUP
            if all(importance[h] <= prune_thresh for h in range(g_start, g_end)):
                ks, ke = kv_h * HEAD_DIM, (kv_h + 1) * HEAD_DIM
                layer.self_attn.k_proj.weight.data[ks:ke, :].zero_()
                layer.self_attn.v_proj.weight.data[ks:ke, :].zero_()

    return int(n_pruned), int(n_scaled)


