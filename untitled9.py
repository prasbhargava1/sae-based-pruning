# ── Linear Probe (PyTorch) ───────────────────────────────────────────────────
# Replaces sklearn LogisticRegression — faster, runs on GPU, no joblib issues.
# Simple 1-layer linear model trained with Adam for 300 steps per layer.

def train_linear_probe(X_train, y_train, X_val, y_val,
                       lr=1e-3, steps=300, batch_size=256):
    """
    X_train: numpy [N, hidden_dim]
    y_train: numpy [N]
    Returns: acc, auc, f1 on validation set
    """
    input_dim = X_train.shape[1]

    # Normalise on CPU before moving to GPU
    mean = X_train.mean(axis=0)
    std  = X_train.std(axis=0) + 1e-8
    X_train = (X_train - mean) / std
    X_val   = (X_val   - mean) / std

    # Convert to tensors
    Xt = torch.tensor(X_train, dtype=torch.float32).to(DEVICE)
    yt = torch.tensor(y_train, dtype=torch.float32).to(DEVICE)
    Xv = torch.tensor(X_val,   dtype=torch.float32).to(DEVICE)

    # Simple linear layer
    probe = torch.nn.Linear(input_dim, 1).to(DEVICE)
    opt   = torch.optim.Adam(probe.parameters(), lr=lr)
    loss_fn = torch.nn.BCEWithLogitsLoss()

    # Mini-batch training
    n = Xt.size(0)
    for step in range(steps):
        idx    = torch.randperm(n, device=DEVICE)[:batch_size]
        logits = probe(Xt[idx]).squeeze(1)
        loss   = loss_fn(logits, yt[idx])
        opt.zero_grad()
        loss.backward()
        opt.step()

    # Evaluate on validation set
    with torch.no_grad():
        logits_val = probe(Xv).squeeze(1).cpu().numpy()
        probs_val  = torch.sigmoid(torch.tensor(logits_val)).numpy()
        preds_val  = (probs_val >= 0.5).astype(int)

    acc = accuracy_score(y_val, preds_val)
    auc = roc_auc_score(y_val, probs_val)
    f1  = f1_score(y_val, preds_val)
    return acc, auc, f1


