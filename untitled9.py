# Percentile thresholds \u2500 applied to each layer's own score distribution
PRUNE_PERCENTILE = 65   # bottom 65% \u2192 zero out
SCALE_PERCENTILE = 80   # 65\u201380%     \u2192 scale down
                        # top 20%    \u2192 keep as-is

# Mistral 7B GQA
