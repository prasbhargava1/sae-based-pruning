# ── Activation Capture via Hooks ──────────────────────────────────────────────
# Hooks are registered at each transformer layer OUTPUT.
# This captures the RESIDUAL STREAM post-MLP — i.e., the full accumulated
# representation after both attention and MLP have written to it at layer i.
#
# Pooling: masked mean over all non-padding token positions.
# More stable than last-token for sentence-level tasks.

def get_mean_pooled_activations(model, tokenizer, texts, batch_size=BATCH_SIZE):
    """
    Run texts through frozen model, return mean-pooled activations per layer.
    Output shape: [num_texts, num_layers, hidden_dim]
    """
    all_layer_activations  = [[] for _ in range(NUM_LAYERS)]
    current_attention_mask = [None]   # mutable container for hook closure
    hooks = []

    def make_hook(layer_idx):
        def hook(module, input, output):
            # output[0]: [batch, seq_len, hidden_dim]  — residual stream post-MLP
            hidden = output[0].float()                        # cast fp16 -> fp32
            mask   = current_attention_mask[0]                # [batch, seq_len]
            mask_f = mask.unsqueeze(-1).float()               # [batch, seq_len, 1]

            # Mean pool: sum over real tokens, divide by token count
            pooled = (hidden * mask_f).sum(dim=1) / mask_f.sum(dim=1)
            # pooled: [batch, hidden_dim]

            all_layer_activations[layer_idx].append(pooled.detach().cpu())
        return hook

    for i, layer in enumerate(model.model.layers):
        hooks.append(layer.register_forward_hook(make_hook(i)))

    with torch.no_grad():
        for start in tqdm(range(0, len(texts), batch_size), desc="Capturing activations"):
            batch_texts = texts[start : start + batch_size]
            encoded = tokenizer(
                batch_texts,
                return_tensors="pt",
                padding=True,
                truncation=True,
                max_length=MAX_LENGTH
            ).to(DEVICE)
            current_attention_mask[0] = encoded["attention_mask"]
            model(**encoded)

    for h in hooks:
        h.remove()

    layer_tensors = [torch.cat(acts, dim=0) for acts in all_layer_activations]
    return torch.stack(layer_tensors, dim=1).numpy()   # [N, 32, 4096]


