# ── Linear Probe ──────────────────────────────────────────────────────────────

class LinearProbe(nn.Module):
    def __init__(self, input_dim):
        super().__init__()
        self.linear = nn.Linear(input_dim, 1)

    def forward(self, x):
        return self.linear(x).squeeze(1)


def train_probe(acts, labels, steps=PROBE_STEPS, lr=PROBE_LR, batch=PROBE_BATCH):
    """Train probe on reconstructed activations (not raw)."""
    mean = acts.mean(axis=0)
    std  = acts.std(axis=0) + 1e-8
    Xt   = torch.tensor((acts - mean) / std, dtype=torch.float32).to(DEVICE)
    yt   = torch.tensor(labels, dtype=torch.float32).to(DEVICE)

    probe   = LinearProbe(acts.shape[1]).to(DEVICE)
    opt     = torch.optim.Adam(probe.parameters(), lr=lr)
    loss_fn = nn.BCEWithLogitsLoss()
    n       = Xt.size(0)

    for _ in range(steps):
        idx  = torch.randperm(n, device=DEVICE)[:batch]
        loss = loss_fn(probe(Xt[idx]), yt[idx])
        opt.zero_grad(); loss.backward(); opt.step()

    # Store normalisation stats for use during ablation
    probe.mean = torch.tensor(mean, dtype=torch.float32).to(DEVICE)
    probe.std  = torch.tensor(std,  dtype=torch.float32).to(DEVICE)
    probe.eval()
    return probe


@torch.no_grad()
def probe_predict(probe, acts_tensor):
    """acts_tensor: raw (un-normalised) [N, hidden_dim] on DEVICE."""
    normed = (acts_tensor - probe.mean) / probe.std
    probs  = torch.sigmoid(probe(normed)).cpu().numpy()
    preds  = (probs >= 0.5).astype(int)
    return probs, preds


# ── Ablation ──────────────────────────────────────────────────────────────────

@torch.no_grad()
def compute_ablation_stats(sae, probe, acts, candidate_indices, batch_size=512):
    """
    For each candidate feature f:
      ablated = full_recon - feat_act[:,f] * W_dec[f]
    Measure flip_rate and score_delta vs baseline.

    Returns stats: [K, 2]  col0=flip_rate  col1=score_delta
    """
    W_dec = sae.W_dec.data    # [dict_size, hidden_dim]
    b_dec = sae.b_dec.data    # [hidden_dim]
    n     = acts.size(0)

    # ── Step 1: encode all examples, compute full reconstruction ──
    all_features = []
    all_recons   = []

    for start in range(0, n, batch_size):
        batch    = acts[start : start + batch_size].to(DEVICE)
        features = sae.encode_only(batch)
        recon    = features @ W_dec + b_dec
        all_features.append(features.cpu())
        all_recons.append(recon.cpu())

    features_all = torch.cat(all_features, dim=0)   # [N, dict_size]
    recons_all   = torch.cat(all_recons,   dim=0)   # [N, hidden_dim]

    # ── Step 2: baseline predictions ──
    baseline_probs, baseline_preds = probe_predict(probe, recons_all.to(DEVICE))

    # ── Step 3: ablate each candidate feature ──
    stats = np.zeros((len(candidate_indices), 2), dtype=np.float32)

    for i, feat_idx in enumerate(tqdm(candidate_indices, desc="  Ablating", leave=False)):
        feat_act     = features_all[:, feat_idx].unsqueeze(1)      # [N, 1]
        feat_dir     = W_dec[feat_idx].unsqueeze(0).cpu()          # [1, hidden_dim]
        contribution = feat_act * feat_dir                         # [N, hidden_dim]
        ablated_recon = recons_all - contribution                  # [N, hidden_dim]

        ablated_probs, ablated_preds = probe_predict(
            probe, ablated_recon.to(DEVICE)
        )

        stats[i, 0] = (ablated_preds != baseline_preds).mean()          # flip_rate
        stats[i, 1] = np.abs(ablated_probs - baseline_probs).mean()     # score_delta

    return stats


