N_KV_HEADS      = 8
HEAD_DIM        = 128
HEADS_PER_GROUP = N_HEADS // N_KV_HEADS   # 4 query heads per KV head

# \u2500\u2500 Google Drive paths \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\n
