@torch.no_grad()
def get_feature_activations(sae, activations, batch_size=ENCODE_BATCH):
    """
    Run activations through SAE encoder in batches.
    activations: [N, hidden_dim]
    returns:     [N, dict_size] sparse feature activations
    No Mistral needed — just SAE encoder.
    """
    all_features = []
    for start in range(0, activations.size(0), batch_size):
        batch    = activations[start : start + batch_size].to(DEVICE)
        features = sae.encode_only(batch).cpu()
        all_features.append(features)
    return torch.cat(all_features, dim=0)


# ── Score Features ────────────────────────────────────────────────────────────

def score_features(feature_acts, labels):
    """
    feature_acts: [N, dict_size] tensor
    labels:       [N] numpy array (0=neg, 1=pos)

    Returns [dict_size, 3]:
      col 0: sentiment_score  = mean(pos) - mean(neg)   signed
      col 1: abs_score        = |sentiment_score|        for ranking
      col 2: activation_freq  = fraction of examples where feature > 0
    """
    labels_t  = torch.tensor(labels, dtype=torch.bool)
    pos_acts  = feature_acts[labels_t]    # [n_pos, dict_size]
    neg_acts  = feature_acts[~labels_t]   # [n_neg, dict_size]

    mean_pos        = pos_acts.mean(dim=0).numpy()
    mean_neg        = neg_acts.mean(dim=0).numpy()
    sentiment_score = mean_pos - mean_neg
    abs_score       = np.abs(sentiment_score)
    act_freq        = (feature_acts > 0).float().mean(dim=0).numpy()

    return np.stack([sentiment_score, abs_score, act_freq], axis=1)


def select_top_features(scores, top_k, min_freq):
    """
    Rank features by abs_score, filter out rarely-firing ones.
    Returns indices of top_k features.
    """
    masked = scores[:, 1].copy()
    masked[scores[:, 2] < min_freq] = 0.0   # zero out rare features
    return np.argsort(masked)[::-1][:top_k].astype(np.int32)


