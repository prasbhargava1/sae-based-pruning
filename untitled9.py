"""
Step 2 — SAE Training for Mistral 7B (All 32 Layers)
------------------------------------------------------
Trains a Sparse Autoencoder on residual stream activations
for every layer in Mistral 7B.

Key design decisions:
  - All 32 layers trained (not just sentiment layers)
    → gives SAE-based importance scores everywhere
    → prune.py can use principled pruning across whole model
  - Early stopping based on reconstruction loss plateau
    → avoids over-training on small datasets
    → typically converges in 5k-10k steps for 2000 examples
  - Activations saved to disk on first run, reused on subsequent runs
    → never re-run Mistral just to retrain an SAE

SAE Architecture:
  Input [4096]
    → subtract b_dec           (centering trick)
    → Linear(4096→32768) + ReLU (encoder)
    → sparse features [32768]
    → Linear(32768→4096)        (decoder, no bias)
    → add b_dec back
  Output reconstruction [4096]

Loss = MSE(input, reconstruction) + L1_COEFF × L1(features)

File structure produced:
  activations/
    layer_{N}_train.npy    shape [N_TRAIN, 4096]
    layer_{N}_val.npy      shape [N_VAL,   4096]
  sae_models/
    sae_layer_{N}.pt       trained SAE weights + config

Estimated training time (all 32 layers, early stopping):
  A100:      ~20 minutes
  T4/Colab:  ~80 minutes
"""

