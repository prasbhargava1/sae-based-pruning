# ── SAE Architecture ──────────────────────────────────────────────────────────

class SparseAutoencoder(nn.Module):
    """
    Sparse Autoencoder for one layer's residual stream.

    b_dec (pre-encoder bias):
      Subtracted before encoding, added after decoding.
      Lets encoder focus on directions not offsets.

    Decoder columns kept unit-norm:
      Prevents large decoder weights from trivially absorbing
      the L1 sparsity penalty.
    """

    def __init__(self, input_dim: int, dict_size: int):
        super().__init__()
        self.input_dim = input_dim
        self.dict_size = dict_size

        self.b_dec = nn.Parameter(torch.zeros(input_dim))
        self.W_enc = nn.Parameter(
            torch.nn.init.kaiming_uniform_(torch.empty(input_dim, dict_size))
        )
        self.b_enc = nn.Parameter(torch.zeros(dict_size))
        self.W_dec = nn.Parameter(
            torch.nn.init.kaiming_uniform_(torch.empty(dict_size, input_dim))
        )
        self._normalize_decoder()

    @torch.no_grad()
    def _normalize_decoder(self):
        self.W_dec.data = F.normalize(self.W_dec.data, dim=1)

    def forward(self, x):
        x_centered     = x - self.b_dec
        features       = F.relu(x_centered @ self.W_enc + self.b_enc)
        reconstruction = features @ self.W_dec + self.b_dec
        return features, reconstruction

    def encode_only(self, x):
        return F.relu((x - self.b_dec) @ self.W_enc + self.b_enc)

    def compute_loss(self, x, features, reconstruction):
        recon_loss    = (x - reconstruction).pow(2).sum(dim=-1).mean()
        sparsity_loss = features.abs().sum(dim=-1).mean()
        total         = recon_loss + L1_COEFF * sparsity_loss
        return total, recon_loss, sparsity_loss


# ── Activation Capture ────────────────────────────────────────────────────────

def capture_and_save_activations(model, tokenizer):
    """
    Run Mistral on train + val data, capture mean-pooled residual stream
    activations at ALL 32 layers in one forward pass.
    Saves each layer's activations as a separate .npy file.

    Hooks fire at transformer layer OUTPUT = post-MLP residual stream.
    Mean pooling over non-padding tokens — more stable than last-token.
    """
    print("Loading IMDB dataset...")
    dataset    = load_dataset("imdb")
    train_data = dataset["train"].shuffle(seed=SEED).select(range(N_TRAIN))
    val_data   = dataset["test"].shuffle(seed=SEED).select(range(N_VAL))

    def capture_split(texts, split_name):
        # One list per layer to accumulate batch results
        layer_acts   = {l: [] for l in TARGET_LAYERS}
        current_mask = [None]
        hooks        = []

        def make_hook(layer_idx):
            def hook(module, input, output):
                hidden = output[0].float()                        # [b, seq, 4096]
                mask   = current_mask[0].unsqueeze(-1).float()    # [b, seq, 1]
                pooled = (hidden * mask).sum(dim=1) / mask.sum(dim=1)  # [b, 4096]
                layer_acts[layer_idx].append(pooled.detach().cpu())
            return hook

        for layer_idx in TARGET_LAYERS:
            h = model.model.layers[layer_idx].register_forward_hook(make_hook(layer_idx))
            hooks.append(h)

        with torch.no_grad():
            for start in tqdm(range(0, len(texts), ACT_BATCH),
                              desc=f"  Capturing {split_name}"):
                batch   = texts[start : start + ACT_BATCH]
                encoded = tokenizer(
                    batch,
                    return_tensors="pt",
                    padding=True,
                    truncation=True,
                    max_length=MAX_LENGTH
                ).to(DEVICE)
                current_mask[0] = encoded["attention_mask"]
                model(**encoded)

        for h in hooks:
            h.remove()

        for layer_idx in TARGET_LAYERS:
            arr  = torch.cat(layer_acts[layer_idx], dim=0).numpy()
            path = os.path.join(ACT_DIR, f"layer_{layer_idx}_{split_name}.npy")
            np.save(path, arr)

        print(f"  Saved {len(TARGET_LAYERS)} layers × {len(texts)} examples → {ACT_DIR}/")

    capture_split(train_data["text"], "train")
    capture_split(val_data["text"],   "val")


def activations_exist():
    """Check all 32 layers × both splits are on disk."""
    for layer_idx in TARGET_LAYERS:
        for split in ["train", "val"]:
            if not os.path.exists(os.path.join(ACT_DIR, f"layer_{layer_idx}_{split}.npy")):
                return False
    return True


def load_activations(layer_idx):
    train = torch.tensor(
        np.load(os.path.join(ACT_DIR, f"layer_{layer_idx}_train.npy")),
        dtype=torch.float32
    )
    val = torch.tensor(
        np.load(os.path.join(ACT_DIR, f"layer_{layer_idx}_val.npy")),
        dtype=torch.float32
    )
    return train, val


# ── Training ──────────────────────────────────────────────────────────────────

def train_sae(layer_idx: int, train_acts: torch.Tensor) -> tuple:
    """
    Train one SAE with early stopping.
    Returns (trained_sae, steps_run, final_recon_loss).
    """
    sae       = SparseAutoencoder(HIDDEN_DIM, DICT_SIZE).to(DEVICE)
    optimizer = torch.optim.Adam(sae.parameters(), lr=LR)
    dataset   = TensorDataset(train_acts)
    loader    = DataLoader(dataset, batch_size=BATCH_SIZE, shuffle=True)
    data_iter = iter(loader)

    # Dead feature tracking
    steps_since_active = torch.zeros(DICT_SIZE, dtype=torch.long)

    # Early stopping state
    best_recon_loss = float('inf')
    patience_count  = 0
    steps_run       = 0

    for step in range(1, MAX_STEPS + 1):
        steps_run = step

        # Cycle data
        try:
            (x,) = next(data_iter)
        except StopIteration:
            data_iter = iter(loader)
            (x,) = next(data_iter)

        x = x.to(DEVICE)
        features, reconstruction = sae(x)
        total, recon_loss, sparsity_loss = sae.compute_loss(x, features, reconstruction)

        optimizer.zero_grad()
        total.backward()
        optimizer.step()
        sae._normalize_decoder()

        # Dead feature tracking
        with torch.no_grad():
            fired = (features > 0).any(dim=0).cpu()
            steps_since_active[fired]  = 0
            steps_since_active[~fired] += 1

        # ── Early stopping check ──
        if step % LOG_EVERY == 0:
            with torch.no_grad():
                mean_active = (features > 0).float().sum(dim=1).mean().item()
                dead_pct    = 100 * (steps_since_active > 10_000).sum().item() / DICT_SIZE
                recon_val   = recon_loss.item()

            print(f"    Step {step:>6} | "
                  f"recon={recon_val:.4f}  "
                  f"l1={sparsity_loss.item():.4f}  "
                  f"active={mean_active:.1f}  "
                  f"dead={dead_pct:.1f}%")

            if step >= MIN_STEPS:
                if recon_val < best_recon_loss - MIN_DELTA:
                    best_recon_loss = recon_val
                    patience_count  = 0
                else:
                    patience_count += 1

                if patience_count >= PATIENCE:
                    print(f"    Early stopping at step {step} "
                          f"(no improvement for {PATIENCE} checks)")
                    break

    return sae, steps_run, best_recon_loss


# ── Evaluation ────────────────────────────────────────────────────────────────

@torch.no_grad()
def evaluate_sae(sae, val_acts, layer_idx):
    """
    Health check metrics on validation activations.
    These verify SAE quality — not sentiment accuracy.

    Metrics:
      Explained variance: 1 - var(residual)/var(input)  → higher is better
      Mean active features: avg features firing per token → lower is sparser
      Dead features %: never fired on val set            → lower is better
    """
    sae.eval()
    val_acts = val_acts.to(DEVICE)
    features, reconstruction = sae(val_acts)

    var_in   = val_acts.var(dim=0).sum().item()
    var_res  = (val_acts - reconstruction).var(dim=0).sum().item()
    expl_var = 1 - var_res / (var_in + 1e-8)

    mean_active = (features > 0).float().sum(dim=1).mean().item()
    dead_pct    = 100 * (features.max(dim=0).values == 0).float().mean().item()

    status = "✓" if expl_var > 0.80 else "⚠"
    print(f"  {status} expl_var={expl_var:.3f}  "
          f"active/token={mean_active:.1f}  "
          f"dead={dead_pct:.1f}%")

    if expl_var < 0.80:
        print(f"    → Low explained variance. Try reducing L1_COEFF ({L1_COEFF}) "
              f"or increasing DICT_SIZE ({DICT_SIZE})")
    if dead_pct > 30:
        print(f"    → Many dead features. Try reducing L1_COEFF or increasing LR")

    sae.train()
    return expl_var, mean_active, dead_pct


