class SparseAutoencoder(nn.Module):
    def __init__(self, input_dim, dict_size):
        super().__init__()
        self.input_dim = input_dim
        self.dict_size = dict_size
        self.b_dec = nn.Parameter(torch.zeros(input_dim))
        self.W_enc = nn.Parameter(torch.empty(input_dim, dict_size))
        self.b_enc = nn.Parameter(torch.zeros(dict_size))
        self.W_dec = nn.Parameter(torch.empty(dict_size, input_dim))


def load_sae_decoder(layer_idx):
    """Load W_dec only — all we need for alignment computation."""
    path       = os.path.join(SAE_DIR, f"sae_layer_{layer_idx}.pt")
    checkpoint = torch.load(path, map_location="cpu")
    sae        = SparseAutoencoder(checkpoint["input_dim"], checkpoint["dict_size"])
    sae.load_state_dict(checkpoint["state_dict"])
    return sae.W_dec.data   # [dict_size, 4096]


# ── Importance Computation ────────────────────────────────────────────────────

def compute_mlp_importance(layer, W_dec_causal):
    """
    Each MLP neuron n's output direction = down_proj.weight[:, n]  [4096]
    importance(n) = Σ_f |cos_sim(neuron_n, feature_f)|
    Normalised to [0, 1].

    down_proj.weight shape: [hidden_dim, intermediate_dim] = [4096, 14336]
    columns = neuron output directions
    """
    down_proj    = layer.mlp.down_proj.weight.data          # [4096, 14336]
    neuron_dirs  = F.normalize(down_proj.T.float(), dim=1)  # [14336, 4096]
    feature_dirs = F.normalize(W_dec_causal.float(), dim=1) # [n_causal, 4096]

    cos_sim    = neuron_dirs @ feature_dirs.T               # [14336, n_causal]
    importance = cos_sim.abs().sum(dim=1)                   # [14336]
    importance = importance / (importance.max() + 1e-8)     # normalise [0,1]

    return importance.numpy()


def compute_head_importance(layer, W_dec_causal):
    """
    Head H's output subspace = o_proj.weight[:, H*128:(H+1)*128]  [4096, 128]
    alignment(H, f) = ||W_O_H.T @ W_dec[f]|| (how much head H can express feature f)
    importance(H)   = mean alignment across all causal features
    Normalised to [0, 1].

    o_proj.weight shape: [hidden_dim, n_heads * head_dim] = [4096, 4096]
    """
    o_proj       = layer.self_attn.o_proj.weight.data       # [4096, 4096]
    feature_dirs = F.normalize(W_dec_causal.float(), dim=1) # [n_causal, 4096]
    importance   = torch.zeros(N_HEADS)

    for h in range(N_HEADS):
        W_O_H      = o_proj[:, h * HEAD_DIM : (h + 1) * HEAD_DIM]  # [4096, 128]
        projection = feature_dirs @ W_O_H                           # [n_causal, 128]
        importance[h] = projection.norm(dim=1).mean()

    importance = importance / (importance.max() + 1e-8)
    return importance.numpy()


