# Percentile thresholds — applied to each layer's own score distribution
PRUNE_PERCENTILE = 70   # bottom 70% → zero out
SCALE_PERCENTILE = 85   # 70–85%     → scale down
                        # top 15%    → keep as-is

# Mistral 7B GQA
N_HEADS         = 32
N_KV_HEADS      = 8
HEAD_DIM        = 128
HEADS_PER_GROUP = N_HEADS // N_KV_HEADS   # 4 query heads per KV head

# ── Google Drive paths ────────────────────────────────────────────────────────

DRIVE_ROOT = "/content/drive/MyDrive/sae_pruning"
IMP_DIR    = f"{DRIVE_ROOT}/importance_scores"
