"""
Step 6 — SAE-Based Structured Pruning
--------------------------------------
Prunes ONLY layers that have SAE importance scores from Step 5.
Layers without scores are left completely untouched.

This keeps pruning principled — every decision is backed by
causal SAE feature analysis. No blind magnitude-based fallback.

As more SAEs are trained (Steps 2-5), re-run this script —
more layers get pruned automatically.

Three-bucket approach per component:
  importance > keep_thresh  → keep as-is       (top 15%)
  prune_thresh < imp < keep → scale down       (middle 15%)
  importance < prune_thresh → zero out         (bottom 70%)

Structured pruning targets:
  MLP neuron n:     gate_proj[n,:], up_proj[n,:], down_proj[:,n]
  Attention head H: q_proj[H*d:(H+1)*d], o_proj[:,H*d:(H+1)*d]
  KV heads:         only if ALL query heads in GQA group are pruned

Inputs:
  {DRIVE_ROOT}/importance_scores/layer_{N}_mlp_importance.npy
  {DRIVE_ROOT}/importance_scores/layer_{N}_head_importance.npy

Output:
  {DRIVE_ROOT}/pruned_model/   HuggingFace model
  {DRIVE_ROOT}/pruned_model/pruning_summary.txt
"""

import os
import torch
import numpy as np
from transformers import AutoModelForCausalLM, AutoTokenizer

# ── Reproducibility ───────────────────────────────────────────────────────────

SEED = 42
torch.manual_seed(SEED)
